package basicprogram;

public class Type_casting {

	public static void main(String[] args) {
int a=10;
double d2 =(double) a;//int into double
System.out.println(d2);


double c2 = 10.11;//double into int
int s4 = (int)c2;
System.out.println(s4);


int v1 = 300;// int into byte
byte g2 = (byte)v1;
System.out.println(g2);
 

double s1= 23.44;//double  into long
long d1 = (long)s1;
System.out.println(d1);
	}

}


